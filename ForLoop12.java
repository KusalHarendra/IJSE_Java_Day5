public class ForLoop12 {
    public static void main(String args[]){
		System.out.println("Start Main");
		for(int i = 0; i< 10; i++){
			System.out.println(i); // Legal
		}
		
		for(int i = 0; i< 10; i++){
			System.out.println(i); // Legal
		}

		for(int i = 0; i< 10; i++){
			System.out.println(i); // Legal
		}
		System.out.println("End Main");         //i can be used in every for loop
    }    
}
